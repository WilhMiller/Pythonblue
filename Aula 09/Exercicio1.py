L = [5, 7, 2, 9, 4, 1, 3]

print(f'O tamanha da lista {L} é: {len(L)} ')
print(f'O maior valor da lista {L} é: {max(L)} ')
print(f'O menor valor da lista {L} é: {min(L)} ')
print(f'O soma da lista é {L} é: {sum(L)} ')
print(f'A ordem crescente da lista é: {sorted(L)} ')
print(f'A ordem decrescente da lista é: {sorted(L, reverse = True)} ')